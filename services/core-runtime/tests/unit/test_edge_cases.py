class TestInsertClipValidation:
    def test_missing_required_fields_rejected(self):
        state = mk_state()
        op = InsertClipOperation(
            op_type="insert_clip", actor="ai",
            payload={"track_id": "track-1"},
        )
        result = op.validate(state)
        assert not result.ok

    def test_source_in_before_available_range_rejected(self):
        state = mk_state()
        op = InsertClipOperation(
            op_type="insert_clip", actor="ai",
            payload={
                "track_id": "track-1", "position": 0,
                "media_ref_id": "media-1",
                "source_in": {"value": -10, "rate": 25},
                "source_out": {"value": 100, "rate": 25},
                "clip_id": "clip-bad",
            },
        )
        v = OperationValidator()
        result = v.validate(op, state)
        assert not result.ok
        assert "before available range" in result.reason

    def test_source_rate_mismatch_rejected(self):
        state = mk_state()
        op = InsertClipOperation(
            op_type="insert_clip", actor="ai",
            payload={
                "track_id": "track-1", "position": 0,
                "media_ref_id": "media-1",
                "source_in": {"value": 0, "rate": 25},
                "source_out": {"value": 100, "rate": 30},
                "clip_id": "clip-bad",
            },
        )
        v = OperationValidator()
        result = v.validate(op, state)
        assert not result.ok


class TestMultipleTracks:
    def test_insert_into_second_track(self):
        state = mk_state()
        track2 = Track(id="track-2", kind="track", schema_version="1.0.0",
                       track_type="audio", item_ids=[], name="A1")
        state.tracks.append(track2)
        engine = EditEngine()
        op = InsertClipOperation(
            op_type="insert_clip", actor="ai",
            payload={
                "track_id": "track-2", "position": 0,
                "media_ref_id": "media-1",
                "source_in": {"value": 0, "rate": 25},
                "source_out": {"value": 50, "rate": 25},
                "clip_id": "audio-clip",
            },
        )
        engine.apply(op, state)
        assert state.tracks[0].item_ids == []
        assert state.tracks[1].item_ids == ["audio-clip"]


class TestStateHashConsistency:
    def test_hash_same_after_json_roundtrip(self):
        from core.serialization.serializer import StateSerializer

        state = mk_state()
        engine = EditEngine()
        op = InsertClipOperation(
            op_type="insert_clip", actor="ai",
            payload={
                "track_id": "track-1", "position": 0,
                "media_ref_id": "media-1",
                "source_in": {"value": 0, "rate": 25},
                "source_out": {"value": 100, "rate": 25},
                "clip_id": "clip-1",
            },
        )
        engine.apply(op, state)
        h1 = state.state_hash
        serializer = StateSerializer()
        json_str = serializer.to_json(state)
        restored = serializer.from_json(json_str)
        h2 = restored.state_hash
        assert h1 == h2


class TestRestoreEntityOperation:
    def test_restore_only_supports_clip_v1(self):
        state = mk_state()
        op = RestoreEntityOperation(
            op_type="restore_entity", actor="ai",
            payload={
                "entity": {
                    "id": "gap-1", "kind": "gap",
                    "schema_version": "1.0.0", "attributes": {},
                    "enabled": True, "duration": {"value": 100, "rate": 25},
                },
            },
        )
        with pytest.raises(ValueError, match="Only clip restore"):
            op.apply(state)
